# Вводим коэффициенты и свободные члены системы уравнений
a1 = float(input("Введите коэффициент a1: "))
b1 = float(input("Введите коэффициент b1: "))
c1 = float(input("Введите свободный член c1: "))
a2 = float(input("Введите коэффициент a2: "))
b2 = float(input("Введите коэффициент b2: "))
c2 = float(input("Введите свободный член c2: "))

# Вычисляем определители матрицы
delta = a1 * b2 - a2 * b1
delta_x = c1 * b2 - c2 * b1
delta_y = a1 * c2 - a2 * c1

# Вычисляем неизвестные x и y
x = delta_x / delta
y = delta_y / delta

# Выводим результаты с использованием форматирования
print("Решение системы уравнений:")
print("x = {:.2f}".format(x))
print("y = {:.2f}".format(y))
