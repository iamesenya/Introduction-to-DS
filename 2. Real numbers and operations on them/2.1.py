import math

# Вводим радиус сферы
R = float(input("Введите радиус сферы: "))

# Рассчитываем площадь большого круга
S = math.pi * R ** 2

# Рассчитываем площадь поверхности сферы
A = 4 * math.pi * R ** 2

# Рассчитываем объем сферы
V = (4/3) * math.pi * R ** 3

# Округляем результаты до двух знаков после запятой
S = round(S, 2)
A = round(A, 2)
V = round(V, 2)

print("Площадь большого круга:", S)
print("Площадь поверхности сферы:", A)
print("Объем сферы:", V)
