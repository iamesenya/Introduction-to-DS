# Создаем множество с произвольными элементами
my_set = {6, 9, 888, 99, 666, 8, 4, 5, 0, 69}

# Преобразуем множество в список для сортировки
sorted_list = sorted(my_set)

# Находим минимальное и максимальное значения
min_value = min(sorted_list)
max_value = max(sorted_list)

print("Сортированный список:", sorted_list)
print("Минимальное значение:", min_value)
print("Максимальное значение:", max_value)

