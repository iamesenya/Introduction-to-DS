# Исходный текст
text = """
    Этот текст содержит несколько предложений.
    Этот текст предназначен для тестирования подсчета частоты слов.
    Подсчет частоты слов поможет понять, какие слова встречаются чаще.
"""

# Разбиваем текст на слова и приводим их к нижнему регистру
words = text.lower().split()

# Создаем словарь для подсчета частоты слов
word_freq = {}

# Подсчитываем частоту слов
for word in words:
    # Удаляем знаки пунктуации
    word = word.strip('.,?!')
    if word in word_freq:
        word_freq[word] += 1
    else:
        word_freq[word] = 1

# Преобразуем словарь в список кортежей (слово, частота)
word_freq_list = list(word_freq.items())

# Сортируем список по частоте встречаемости слов
word_freq_list.sort(key=lambda x: x[1], reverse=True)

# Выводим три наиболее часто встречающихся слова
print("Три наиболее часто встречающихся слова:")
for word, freq in word_freq_list[:3]:
    print(f"{word}: {freq} раза")
