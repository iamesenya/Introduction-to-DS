# Вводим высоту, длину и ширину параллелепипеда
h = float(input("Введите высоту параллелепипеда: "))
a = float(input("Введите длину стороны a основания: "))
b = float(input("Введите длину стороны b основания: "))

# Рассчитываем площадь основания (прямоугольника)
base_area = a * b

# Рассчитываем объем параллелепипеда
volume = base_area * h

# Выводим результаты с округлением до 2 знаков после запятой
print("Площадь основания = {:.2f} кв.ед.".format(base_area))
print("Объем фигуры = {:.2f} куб.ед.".format(volume))
