n = int(input("Введите натуральное число от 1 до 9: "))

# Проверяем, что число находится в нужном интервале
if 1 <= n <= 9:
    # Рассчитываем n - 1 и n + 1
    n_minus_1 = n - 1
    n_plus_1 = n + 1

    # Делим полученные значения на 10 и складываем результаты
    result = (n_minus_1 / 10) + (n_plus_1 / 10)

    # Округляем результат до 2 знаков после запятой
    rounded_result = round(result, 2)
    print("Результат:", rounded_result)
else:
    print("Введите натуральное число от 1 до 9.")
