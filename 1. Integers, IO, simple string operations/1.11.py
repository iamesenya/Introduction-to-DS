# Вводим строку из натуральных чисел
s = input("Введите строку из натуральных чисел от 0 до 9: ")

# Проверяем, что строка содержит только цифры
if s.isdigit():
    # Элементы строки с 2 по 3 (индексы 1 и 2)
    print("Элементы с 2 по 3:", s[1:3])

    # Элемент строки с индексом -2 (предпоследний элемент)
    print("Элемент с индексом -2:", s[-2])

    # Элементы строки с 0 по 4 (первые 5 элементов)
    print("Элементы с 0 по 4:", s[0:5])

    # Все четные элементы строки
    print("Все четные элементы:", s[1::2])

    # Все нечетные элементы строки
    print("Все нечетные элементы:", s[0::2])

    # Все элементы строки за исключением двух последних
    print("Все элементы за исключением двух последних:", s[:-2])

    # Все элементы в обратном порядке
    print("Все элементы в обратном порядке:", s[::-1])

    # Все нечетные элементы в обратном порядке
    print("Все нечетные элементы в обратном порядке:", s[-1::-2])

    # Все четные элементы в обратном порядке
    print("Все четные элементы в обратном порядке:", s[-2::-2])

    # Длина строки
    print("Длина строки:", len(s))
else:
    print("Введите строку, состоящую из натуральных чисел от 0 до 9.")
